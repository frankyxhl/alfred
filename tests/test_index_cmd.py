def test_index_has_separator_before_table(tmp_path):
    """Generated index has --- separator between metadata and body."""
    content = _get_index_content(tmp_path)
    lines = content.split("\n")
    # There should be a --- after metadata, before the table
    sep_indices = [i for i, line in enumerate(lines) if line.strip() == "---"]
    assert len(sep_indices) >= 1, "Expected at least one --- separator"


def test_index_passes_validate(tmp_path):
    """Generated index passes af validate (no issues for the index doc)."""
    _get_index_content(tmp_path)
    runner = CliRunner()
    result = runner.invoke(
        cli, ["validate", "--root", str(tmp_path)], catch_exceptions=False
    )
    # Index docs (ACID=0000) should not produce any issues
    assert "TST-0000" not in result.output or "0 issues" in result.output


def test_index_multiple_prefixes_compliant(tmp_path):
    """Generated indexes for multiple prefixes are all compliant."""
    rules_dir = tmp_path / "rules"
    rules_dir.mkdir()
    (rules_dir / "ALF-2100-SOP-Something.md").write_text("# ALF doc")
    (rules_dir / "NRV-2200-SOP-Other.md").write_text("# NRV doc")
    runner = CliRunner()
    runner.invoke(cli, ["index", "--root", str(tmp_path)], catch_exceptions=False)
    alf_content = (rules_dir / "ALF-0000-REF-Document-Index.md").read_text()
    nrv_content = (rules_dir / "NRV-0000-REF-Document-Index.md").read_text()
    assert "**Applies to:** ALF project" in alf_content
    assert "**Applies to:** NRV project" in nrv_content
    assert alf_content.startswith("# REF-0000: Document Index\n")
    assert nrv_content.startswith("# REF-0000: Document Index\n")
