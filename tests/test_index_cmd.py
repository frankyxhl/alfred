def _get_index_content(tmp_path) -> str:
    """Helper: create a project with one doc, run index, return index content."""
    rules_dir = tmp_path / "rules"
    rules_dir.mkdir(exist_ok=True)
    (rules_dir / "TST-2100-SOP-Something.md").write_text("# test")
    runner = CliRunner()
    runner.invoke(cli, ["index", "--root", str(tmp_path)], catch_exceptions=False)
    return (rules_dir / "TST-0000-REF-Document-Index.md").read_text()


def test_index_h1_format(tmp_path):
    """Generated index has compliant H1: # REF-0000: Document Index."""
    content = _get_index_content(tmp_path)
    assert content.startswith("# REF-0000: Document Index\n")


def test_index_has_applies_to(tmp_path):
    """Generated index has **Applies to:** field with correct prefix."""
    content = _get_index_content(tmp_path)
